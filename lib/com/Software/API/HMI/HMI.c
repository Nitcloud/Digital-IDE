//******************************************
//函数名称：HMICommandPut
//函数功能：串口输出指令	使用UART4
//参    数：const uint8_t *Data	-- 字符串数据
//返还值：  无
//******************************************
void HMICommandPut(const char *Data)
{
	UARTprintf(1,"%s",Data);
	UARTprintf(1,FINISH);
}

//******************************************
//函数名称：HMIDrawCurve
//函数功能：绘制曲线	使用UART4
//参    数：int Num   --曲线中包含点的个数
//		    const uint8_t *Data -- 数据存放数组的首地址
//返还值：  无
//******************************************
void HMIDrawCurve(const unsigned char *Data ,int Num)
{
	int index;
	uint16_t temp;
	
	for(index=0;index<Num;index++)
	{	
		char str[14] = {0};
		temp = (uint16_t)((*(Data+index))/2+60);
		sprintf(str,"add 10,0,%d",temp);	//曲线ID号：10 通道：0
		HMICommandPut(str);
	}
		
}

